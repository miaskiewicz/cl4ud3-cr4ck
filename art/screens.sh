NUM_SCREENS=${#SCREENS[@]}
# Load custom art from ~/.cl4ud3-cr4ck/art/custom/*.txt
CL4UD3_HOME="${CL4UD3_HOME:-$HOME/.cl4ud3-cr4ck}"
[ -f "$CL4UD3_HOME/config.sh" ] && source "$CL4UD3_HOME/config.sh"
if [ "$CL4UD3_CUSTOM_ART" != "false" ] && [ -d "$CL4UD3_HOME/art/custom" ]; then
    for artfile in "$CL4UD3_HOME/art/custom"/*.txt; do
        [ -f "$artfile" ] && SCREENS+=("$(cat "$artfile")")
    done
    NUM_SCREENS=${#SCREENS[@]}
fi

RANDOM_INDEX=$((RANDOM % NUM_SCREENS))
# Write directly to terminal — bypasses Claude Code's stdout/stderr capture
# Then output matching newlines to stderr so Claude Code advances its cursor
ART="${SCREENS[$RANDOM_INDEX]}"
LINE_COUNT=$(echo -e "$ART" | wc -l)
if [ -w /dev/tty ]; then
    printf '\n' > /dev/tty
    if [ "$CL4UD3_STARTUP_ANIMATION" != "false" ]; then
        # Scanline reveal — line-by-line with CRT delay (~0.6s total)
        echo -e "$ART" | while IFS= read -r line; do
            printf '%s\n' "$line" > /dev/tty
            sleep 0.03
        done
    else
        echo -e "$ART" > /dev/tty
    fi
    printf '\n' > /dev/tty
    # Push Claude Code's cursor down to match the art height (+15 for tall art)
    printf '\n%.0s' $(seq 1 $((LINE_COUNT + 15))) >&2
else
    printf '\n' >&2
    echo -e "$ART" >&2
    printf '\n' >&2
fi
